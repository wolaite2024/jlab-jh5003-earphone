static void ST7801_Reg_Write(const ST7801_CMD_DESC *cmd)
{
    uint16_t idx = 0;

    uint8_t *sdat = os_mem_alloc(OS_MEM_TYPE_DATA, 100);
    while (cmd[idx].instruction != ST7801_QSPI_SEQ_FINISH_CODE)
    {
        sdat[0] = cmd[idx].instruction;

        sdat[1] = 0;
        sdat[2] = cmd[idx].index; // Set in the middle 8 bits ADDR[15:8] of the 24 bits ADDR[23:0]
        sdat[3] = 0;

        for (uint16_t i = 0; i < cmd[idx].wordcount; i++)
        {
            sdat[i + 4] = cmd[idx].payload[i];
        }

        lcd_write(sdat, cmd[idx].wordcount + 4);
        if (cmd[idx].delay != 0)
        {
            platform_delay_ms(cmd[idx].delay);
        }

        idx++;
    }
    os_mem_free(sdat);
}

void ST7801_Init_Post_OTP(void)
{
    ST7801_Reg_Write(ST7801_POST_OTP_POWERON_SEQ_CMD);
}

static void rtl_ST7801_qspi_cmd_param4(uint8_t cmd,
                                       uint8_t *data) //total 8 byte, first byte is 0x02
{
    uint8_t sdat[] = {ST7801_QSPI_INST_CMD_WRITE, 0x00, cmd, 0x00, data[0], data[1], data[2], data[3]};
    lcd_write(sdat, sizeof(sdat));
}

void lcd_ST7801_qspi_368_448_set_window(uint16_t xStart, uint16_t yStart, uint16_t xEnd,
                                        uint16_t yEnd)
{
    uint8_t data[4];
    data[0] = xStart >> 8;
    data[1] = xStart & 0xff;
    data[2] = xEnd >> 8;
    data[3] = xEnd & 0xff;
    rtl_ST7801_qspi_cmd_param4(0x2A, data);

    data[0] = yStart >> 8;
    data[1] = yStart & 0xff;
    data[2] = yEnd >> 8;
    data[3] = yEnd & 0xff;
    rtl_ST7801_qspi_cmd_param4(0x2B, data);

    uint32_t len_byte = (xEnd - xStart + 1) * (yEnd - yStart + 1) * OUTPUT_PIXEL_BYTES;
    lcd_enter_data_output_mode(len_byte);
}

void st7801_init(void)
{
    ST7801_Init_Post_OTP();
}
