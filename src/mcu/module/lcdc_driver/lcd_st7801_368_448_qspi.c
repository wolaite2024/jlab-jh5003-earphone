typedef struct _ST7801_CMD_DESC
{
    uint8_t instruction;
    uint8_t index;
    uint16_t delay;
    uint16_t wordcount;
    uint8_t  payload[ST7801_MAX_PARA_COUNT];
} ST7801_CMD_DESC;

static uint8_t lcd_vci_en_pin;

void lcd_vci_en_pin_config(uint8_t lcd_vci_en)
{
    lcd_vci_en_pin = lcd_vci_en;
}

void lcd_vci_en_init(void)
{
    hal_gpio_init();
    hal_gpio_init_pin(lcd_vci_en_pin, GPIO_TYPE_AON, GPIO_DIR_OUTPUT, GPIO_PULL_DOWN);
    hal_gpio_set_level(lcd_vci_en_pin, GPIO_LEVEL_LOW);
}

void lcd_set_vci_en(bool set)
{
    if (set)
    {
        hal_gpio_set_level(lcd_vci_en_pin, GPIO_LEVEL_HIGH);
    }
    else
    {
        hal_gpio_set_level(lcd_vci_en_pin, GPIO_LEVEL_LOW);
    }
}

